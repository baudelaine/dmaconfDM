package com.dma.web;


public class Metrics {

	int qsCount;
	int finCount;
	int refCount;
	int dimensionCount;
	int measureCount;
	int customCount;
	
	public int getQsCount() {
		return qsCount;
	}
	public void setQsCount(int qsCount) {
		this.qsCount = qsCount;
	}
	public int getFinCount() {
		return finCount;
	}
	public void setFinCount(int finCount) {
		this.finCount = finCount;
	}
	public int getRefCount() {
		return refCount;
	}
	public void setRefCount(int refCount) {
		this.refCount = refCount;
	}
	public int getDimensionCount() {
		return dimensionCount;
	}
	public void setDimensionCount(int dimensionCount) {
		this.dimensionCount = dimensionCount;
	}
	public int getMeasureCount() {
		return measureCount;
	}
	public void setMeasureCount(int measureCount) {
		this.measureCount = measureCount;
	}
	public int getCustomCount() {
		return customCount;
	}
	public void setCustomCount(int customCount) {
		this.customCount = customCount;
	}
	
}
